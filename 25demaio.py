# @title Um exemplo pratico de criação de classe

class Pessoa:
    def __init__(self, nome:str,idade:int,altura:float):
        self.nome = nome
        self.idade = idade
        self.altura = altura

    def dizer_ola(self):
        print(f'ola, meu nome é {self.nome}\n')
        print(f'Tenho {self.idade} anos de idade')
        print(f'E minha altura é {self.altura}')

    def cozinhar(self, receita:str):
        print(f'Estou cozinhando um(a) {receita}')

    def andar(self, distancia:float):
        print(f'Sai para andar, Volto depois de completar {distancia} metros...')

