class Televisao():
    def __init__(self, min, max):
       self.ligada=False
       self.canal=2
       self.cmin = min
       self.cmax = max

    def muda_canal_para_baixo(self):
        if self.canal -1 >= self.cmin:
            self.canal -= 1
            print('canal alterado')
        else:
            print('Não pode acessar essa faxa de canais')


    def muda_canal_para_cima(self):
        if self.canal + 1 <= self.cmax:
            self.canal += 1
            print('Canal alterado')
        else:
            print('Não pode acessar essa faxa de canais')

